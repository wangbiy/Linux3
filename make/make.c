#include<stdio.h>
#include <unistd.h>
#include <string.h>

int  main()
{
    int i = 1;
    char bar[101];
    memset(bar, '\0', sizeof(bar));
    const char *lable = "|/-\\";
    while(i <= 100)
    {
        bar[i - 1] = ' ';
        if(i <= 25)
            printf("[%d%%]\33[44m%-s\33[0m[%c]\r", i, bar, lable[i % 4]);

        if(i > 25 && i <= 50)
            printf("[%d%%]\33[45m%-s\33[0m[%c]\r", i, bar, lable[i % 4]);

        if(i > 50 && i <= 75)
            printf("[%d%%]\33[42m%-s\33[0m[%c]\r", i, bar, lable[i % 4]);

        if(i > 75 && i <= 100)
            printf("[%d%%]\33[47m%-s\33[0m[%c]\r", i, bar, lable[i % 4]);
        fflush(stdout);
        i++;
        usleep(100000);
    }
    printf("\nOK!\n");
    return 0;
}
